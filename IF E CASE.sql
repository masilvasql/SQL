
/*IF - ELSE*/
DECLARE 
	@NUMERO INT = 10
BEGIN
	IF @NUMERO = 10
		PRINT 'VALOR VERDADEIRO'
	ELSE
		PRINT 'VALOR FALSO'
END
GO

/*CASE - ELE REPRESENTA UMA COLUNA*/
DECLARE 
	@NUMERO INT
BEGIN
	SELECT 
		CASE
			WHEN FABRICANTE = 'FIAT' THEN 'FAIXA 1'
			WHEN FABRICANTE = 'CHEVROLET' THEN	'FAIXA 2'
			ELSE 'OUTRAS FAIXAS'
		END AS FAIXAPRECO,
		*
	FROM
		CARROS
END
GO
